function findEncIndex(thisArray, checkLocation){
    for(i=0; i<thisArray.length; i++){
        if(thisArray[i][0][0]==checkLocation){
            return i
        }
    }
    return -1
}

function returnHashedArray(checkLocation){
    if(typeof checkLocation == 'string'){
        locName=checkLocation
    } else {
        locName=AllEncounters[checkLocation][0][0];
    }
    letterNumber=locName.charCodeAt(0)-65;
    for(i=0; i<hashedEncounters[letterNumber].length; i++){
        if(hashedEncounters[letterNumber][i][0][0]==locName){
            return hashedEncounters[letterNumber][i];
        }
    }
}

function findEncTypeIndex(thisArray, checkType){
    for(i=0; i<thisArray.length; i++){
        if(thisArray[i]==checkType){
            return i
        }
    }
    return -1
}